export function simulatePriceChange(stock: Stock): Stock {
  // Simulate Â±0.5% to 3% change
  const changePercent = (Math.random() - 0.5) * 6;
  const newPrice = stock.currentPrice * (1 + changePercent / 100);
  
  return {
    ...stock,
    currentPrice: Math.round(newPrice * 100) / 100,
    change: Math.round((newPrice - stock.basePrice) * 100) / 100,
    changePercent: Math.round((((newPrice - stock.basePrice) / stock.basePrice) * 100) * 100) / 100,
  };
}
